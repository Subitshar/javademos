
public class o7operator {

	public static void main(String[] args) {
		int result=1+9;
		System.out.println(result);
		System.out.println();
		result=result-3;
		System.out.println(result);
		System.out.println();
		result=result/2;
		System.out.println(result);
		System.out.println();
		result=result*3;
		System.out.println(result);
		System.out.println();
		result=result%2;
		System.out.println(result);
		System.out.println();
		result++;
		System.out.println(result);
		System.out.println();
		result--;
		System.out.println(result);
		
	}

}
